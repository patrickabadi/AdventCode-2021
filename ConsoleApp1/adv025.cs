        private int RunSteps(char[,] grid)
        {
            int h = grid.GetLength(0);
            int w = grid.GetLength(1);

            int steps = 0;

            int numMove = 1;
            while (numMove != 0)
            {
                numMove = 0;

                for (int row = 0; row < h; row++)
                {
                    bool hadItem = false;
                    for (int col = 0; col < w; col++)
                    {
                        var current = grid[row, col];
                        if (current != '>')
                            continue;

                        if (col == 0)
                            hadItem = true;

                        var nextIndex = col < w - 1 ? col + 1 : 0;
                        var next = grid[row, nextIndex];
                        if (next != '.')
                            continue;

                        if (col == w - 1 && hadItem)
                            continue;

                        grid[row, col] = '.';
                        grid[row, nextIndex] = '>';
                        numMove++;
                        col++;
                    }
                }

                for (int col = 0; col < w; col++)
                {
                    bool hadItem = false;
                    for (int row = 0; row < h; row++)
                    {
                        var current = grid[row, col];
                        if (current != 'v')
                            continue;

                        if (row == 0)
                            hadItem = true;

                        var nextIndex = row < h - 1 ? row + 1 : 0;
                        var next = grid[nextIndex, col];
                        if (next != '.')
                            continue;

                        if (row == h - 1 && hadItem)
                            continue;

                        grid[row, col] = '.';
                        grid[nextIndex, col] = 'v';
                        numMove++;
                        row++;
                    }
                }


                steps++;

            }

            //DebugGrid(grid);

            return steps;
        }

        void DebugGrid(char[,] grid)
        {
            int h = grid.GetLength(0);
            int w = grid.GetLength(1);
            for (int row = 0; row < h; row++)
