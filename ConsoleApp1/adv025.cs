        }

        char[,] Parse(string inp)
        {
            var lines = inp.Split('\n');
            var w = lines[0].Trim().Length;
            var h = lines.Length;

            var grid = new char[h, w];
            for (int row = 0; row < h; row++)
            {
                var line = lines[row];
                for (int col = 0; col < w; col++)
                {
                    grid[row, col] = line[col];
                }
            }

            return grid;
        }

