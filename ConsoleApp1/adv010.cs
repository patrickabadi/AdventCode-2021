        private long FindCompletes(string [] lines)
        {
            List<long> incompleteScores = new List<long>();
            List<char> chain = new List<char>();
            char expectedCloser = ' ';

            foreach (var line in lines)
            {
                int width = line.Trim().Length;
                chain.Clear();
                expectedCloser = ' ';

                for (int i = 0; i < width; i++)
                {
                    var checker = line[i];

                    if (IsOpener(checker))
                    {
                        chain.Add(checker);
                        expectedCloser = GetCloser(checker);
                    }
                    else if (checker == expectedCloser)
                    {
                        chain.RemoveAt(chain.Count - 1);
                        expectedCloser = chain.Any() ? GetCloser(chain.Last()) : ' ';
                    }
                    else
                    {
                        // corruptions
                        chain.Clear();
                        break;
                    }
                }

                if(chain.Any())
                {
                    long t = 0;
                    chain.Reverse();
                    foreach(var c in chain)
                    {
                        t *= 5;
                        t += ClosingPoints(GetCloser(c));
                        Debug.Write($"{GetCloser(c)}");
                    }
                    incompleteScores.Add(t);
                    Debug.WriteLine($" - CompletionPoints {t}");
                }
                else
                {
                    //Debug.WriteLine($"CompletionPoints {0}");
                }
            }

            incompleteScores.Sort();



            return incompleteScores[incompleteScores.Count/2];
        }

        private int FindCorrupted(string[] lines)
        {
            int total = 0;
            List<char> chain = new List<char>();
            char expectedCloser = ' ';

            foreach(var line in lines)
            {
                int width = line.Trim().Length;
                chain.Clear();
                expectedCloser = ' ';

                for (int i=0; i<width; i++)
                {
                    var checker = line[i];
                    

                    if (IsOpener(checker))
                    {
                        chain.Add(checker);
                        expectedCloser = GetCloser(checker);
                    }
                    else if(checker == expectedCloser)
                    {
                        chain.RemoveAt(chain.Count - 1);
                        expectedCloser = chain.Any() ? GetCloser(chain.Last()) : ' ';
                    }
                    else
                    {
                        // corruptions
                        Debug.WriteLine($"Expected {expectedCloser} but found {checker}");
                        total += CorruptedPoints(checker);
                        break;
                    }
                }
            }


            return total;
        }

        int ClosingPoints(char check)
        {
            switch (check)
            {
                case ')':
                    return 1;
                case ']':
                    return 2;
                case '}':
                    return 3;
                case '>':
                    return 4;
                default:
                    throw new Exception($"ClosingPoints invalid {check}");
            }
        }
        int CorruptedPoints(char check)
        {
            switch(check)
            {
                case ')':
                    return 3;
                case ']':
                    return 57;
                case '}':
                    return 1197;
                case '>':
                    return 25137;
                default:
                    throw new Exception($"GetCorruptedPoints invalid {check}");
            }
        }

        char GetCloser(char opener)
        {
            switch (opener)
            {
                case '(':
                    return ')';
                case '[':
                    return ']';
                case '{':
                    return '}';
                case '<':
                    return '>';
                default:
                    throw new Exception($"{opener} is not an opener");
            }
        }

        bool IsOpener(char check)
        {
            switch (check)
            {
                case '(':
                case '[':
                case '{':
                case '<':
                    return true;
                default:
                    return false;
            }
        }

